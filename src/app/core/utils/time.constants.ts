/**
 * Time conversion constants in milliseconds.
 * Use these instead of inline calculations for clarity and consistency.
 */
export const MS_PER_SECOND = 1000;
export const MS_PER_MINUTE = 60 * MS_PER_SECOND;
export const MS_PER_HOUR = 60 * MS_PER_MINUTE;
export const MS_PER_DAY = 24 * MS_PER_HOUR;
export const MS_PER_WEEK = 7 * MS_PER_DAY;
export const MS_PER_MONTH = 30.44 * MS_PER_DAY; // Average days per month
export const MS_PER_YEAR = 365.25 * MS_PER_DAY; // Account for leap years
